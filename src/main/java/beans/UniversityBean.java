package beans;

import java.util.List;

public class UniversityBean {
	private int id;
	private String name;
	private String country;
	private List<Field> fields;
	
	public UniversityBean() {
		
	}

	public String getName() {
		return name;
	}

	public String getCountry() {
		return country;
	}

	public List<Field> getFields() {
		return fields;
	}

	public int getId() {
		return id;
	}
	
	
	
	
	
	
	

}
